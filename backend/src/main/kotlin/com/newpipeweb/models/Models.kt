/**
 * Models.kt
 *
 * All serializable data classes used as API request/response bodies.
 * These are shared between routes and services, and serialized to JSON
 * by Ktor's kotlinx.serialization integration.
 *
 * Every model that crosses the API boundary must be annotated @Serializable.
 */

package com.newpipeweb.models

import kotlinx.serialization.Serializable

// ─────────────────────────────────────────────
// Video & Search models
// ─────────────────────────────────────────────

/**
 * Represents a single video/track/stream item from any service.
 * `service` indicates which platform it came from (e.g. "youtube", "soundcloud").
 */
@Serializable
data class VideoModel(
    val id: String,
    val title: String,
    val uploader: String,
    val uploaderUrl: String,
    val duration: Long,           // seconds; -1 for live streams
    val viewCount: Long,
    val uploadDate: String,
    val thumbnailUrl: String,
    val description: String = "",
    val isLive: Boolean = false,
    val url: String,
    val service: String = "youtube" // which platform this item is from
)

/**
 * Search results for a query on a specific service.
 */
@Serializable
data class SearchModel(
    val query: String,
    val service: String,          // which service was searched
    val items: List<VideoModel>,
    val nextPage: String? = null  // URL for pagination (if supported)
)

// ─────────────────────────────────────────────
// Stream model — playable URLs for a piece of content
// ─────────────────────────────────────────────

/**
 * Full stream info for a video/track — returned by GET /stream.
 * Contains all available quality options + subtitles + related content.
 */
@Serializable
data class StreamModel(
    val id: String,
    val title: String,
    val uploader: String,
    val uploaderUrl: String = "",
    val duration: Long,
    val videoStreams: List<StreamUrl>,
    val audioStreams: List<StreamUrl>,
    val hlsUrl: String? = null,        // for live streams
    val subtitles: List<SubtitleTrack>,
    val relatedVideos: List<VideoModel>,
    val service: String = "youtube",
    val thumbnailUrl: String = ""
)

/** A single playable stream URL with quality metadata */
@Serializable
data class StreamUrl(
    val url: String,
    val quality: String,       // e.g. "1080p", "720p", "128kbps"
    val format: String,        // e.g. "VIDEO_WEBM_VP9", "M4A"
    val isVideoOnly: Boolean = false
)

/** A subtitle/caption track with language info */
@Serializable
data class SubtitleTrack(
    val url: String,
    val languageCode: String,
    val languageName: String,
    val isAutoGenerated: Boolean
)

// ─────────────────────────────────────────────
// Channel / Artist model
// ─────────────────────────────────────────────

/**
 * Channel info — works for YouTube channels, SoundCloud artists,
 * PeerTube channels, Bandcamp artists, etc.
 */
@Serializable
data class ChannelModel(
    val id: String,
    val name: String,
    val url: String,
    val description: String,
    val subscriberCount: Long,
    val avatarUrl: String,
    val bannerUrl: String,
    val videos: List<VideoModel>,
    val service: String = "youtube"
)

// ─────────────────────────────────────────────
// Comment model (YouTube only)
// ─────────────────────────────────────────────

@Serializable
data class CommentModel(
    val id: String,
    val author: String,
    val authorAvatarUrl: String,
    val text: String,
    val likeCount: Int,
    val isHeartedByUploader: Boolean,
    val isPinned: Boolean,
    val publishedDate: String
)

@Serializable
data class CommentsModel(
    val videoId: String,
    val comments: List<CommentModel>,
    val nextPage: String? = null
)

// ─────────────────────────────────────────────
// History model
// ─────────────────────────────────────────────

@Serializable
