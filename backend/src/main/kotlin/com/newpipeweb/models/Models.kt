    val id: Int,
    val videoId: String,
    val title: String,
    val uploader: String,
    val thumbnailUrl: String,
    val filePath: String,
    val fileSize: Long,
    val downloadedBytes: Long,
    val status: String,            // PENDING | DOWNLOADING | COMPLETED | FAILED
    val quality: String,
